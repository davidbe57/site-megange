    <h2>1. Accéder à l'administration</h2>
            <p>Pour accéder au panneau d'administration :</p>
            <ol>
                <li>Ouvrez votre navigateur (Chrome, Firefox, Edge)</li>
                <li>Allez sur <strong>https://village-megange.fr/admin</strong></li>
                <li>Entrez le <strong>mot de passe administrateur</strong> (communiqué par le webmaster)</li>
                <li>Cliquez sur <strong>Connexion</strong></li>
            </ol>

            <h2>2. Le tableau de bord</h2>
            <p>Une fois connecté, vous arrivez sur le tableau de bord qui affiche :</p>
            <ul>
                <li><strong>Visiteurs</strong> : nombre de visiteurs aujourd'hui, cette semaine, ce mois</li>
                <li><strong>Derniers messages</strong> : les messages reçus via le formulaire de contact</li>
                <li><strong>Accès rapide</strong> : liens vers toutes les pages d'administration</li>
            </ul>

            <h2>3. Gérer les contenus</h2>

            <h3>Bulletins municipaux</h3>
            <p>Ajouter, modifier ou supprimer les bulletins PDF :</p>
            <ul>
                <li>Cliquez sur <strong>Bulletins</strong> dans le menu</li>
                <li>Cliquez sur <strong>Ajouter un bulletin</strong></li>
                <li>Choisissez un fichier PDF, une date et une miniature (optionnelle)</li>
                <li>Cliquez sur <strong>Enregistrer</strong></li>
            </ul>

            <h3>Comptes rendus du conseil</h3>
            <p>Pareil que les bulletins : ajoutez des PDF de comptes rendus de réunions du conseil municipal.</p>

            <h3>Élus</h3>
            <p>Gérer la liste des membres du conseil municipal :</p>
            <ul>
                <li>Cliquez sur <strong>Élus</strong></li>
                <li>Remplissez le nom, le rôle (Maire, Adjoint, Conseiller), la délégation</li>
                <li>Vous pouvez aussi ajouter une photo</li>
            </ul>

            <h3>Actualités / Articles</h3>
            <ul>
                <li>Cliquez sur <strong>Articles</strong></li>
                <li>Écrivez un titre et le contenu de l'actualité</li>
                <li>Vous pouvez y mettre des photos et des liens</li>
                <li>L'article apparaîtra sur la page Actualités du site</li>
            </ul>

            <h3>Carrousel (page d'accueil)</h3>
            <p>Les images qui défilent en haut de la page d'accueil :</p>
            <ul>
                <li>Cliquez sur <strong>Carrousel</strong></li>
                <li>Ajoutez une image (format paysage recommandé)</li>
                <li>Vous pouvez mettre un titre et un lien</li>
            </ul>

            <h3>Galerie photos</h3>
            <p>Ajoutez des photos du village pour la page galerie.</p>

            <h2>4. Gérer les inscriptions</h2>
            <p>Depuis <strong>Abonnés</strong>, vous pouvez :</p>
            <ul>
                <li>Voir la liste des personnes inscrites sur le site</li>
                <li>Modifier leurs informations</li>
                <li>Supprimer un compte</li>
                <li><strong>Exporter</strong> la liste au format CSV (pour l'ouvrir dans Excel)</li>
            </ul>

            <h2>5. Envoyer un email aux abonnés</h2>
            <ul>
                <li>Cliquez sur <strong>Envoyer un article</strong></li>
                <li>Écrivez un titre et votre message</li>
                <li>Cliquez sur <strong>Envoyer</strong> : l'email sera envoyé à tous les abonnés</li>
            </ul>

            <h2>6. Modifier les informations de la mairie</h2>
            <p>Depuis <strong>Configuration</strong>, vous pouvez mettre à jour :</p>
            <ul>
                <li>Le numéro de téléphone</li>
                <li>L'adresse email</li>
                <li>L'adresse postale</li>
                <li>Les horaires d'ouverture</li>
            </ul>

            <h2>7. Voir les statistiques</h2>
            <p>Depuis <strong>Statistiques</strong>, vous voyez :</p>
            <ul>
                <li>Le nombre de visites (aujourd'hui, semaine, mois, total)</li>
                <li>Les pages les plus consultées</li>
                <li>D'où viennent les visiteurs (Google, Facebook, direct…)</li>
                <li>Les navigateurs et appareils utilisés</li>
            </ul>

            <h2>8. Se déconnecter</h2>
            <p>Pensez à cliquer sur <strong>Déconnexion</strong> dans le menu quand vous avez fini, surtout si vous utilisez un ordinateur partagé.</p>

